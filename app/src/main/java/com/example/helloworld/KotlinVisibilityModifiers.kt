Q- What is Visibility Modifiers?
Visibility Modifiers are the keywords which are used to restrict the use of classes, interfaces, methods, and properties in Kotlin.

These modifiers are used at multiple places such as class header or method body.

Visibility modifiers are categorized into four different types:
    1.public - add 'public' keyword before any function definition or member
    private  - add 'private' ,,    ,,     ,,
    protected
    internal - add 'internal'
    - The internal modifier is special feature of Kotlin and makes the field visible only inside the module in which it is implemented

